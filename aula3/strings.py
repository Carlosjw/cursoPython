''''
str - string: São textos que estão dentro de aspas simples ou duplas
'''

# IMPRIMIR TEXTOS ENTRE ASPAS:

# Intercalando entre aspas simples e duplas
print('Fulano é um "pé no saco".')
print("Fulano é um 'pé no saco'.")

# Usando caractere de escape
print('Esse é meu \'texto\' (str).')
print("Esse é meu \"texto\" (str).")

# QUEBRANDO TEXTO;
print('Esse é meu \n (str).')  # Usando \n para a quebra
print(r'Esse é meu \n (str).')  # Ignorando o \n com o interpolador r antes das aspas abrirem

