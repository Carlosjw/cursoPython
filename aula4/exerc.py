#  EXERCÍCIO BÁSICO:

# Nome: String(str)
print('Carlos', type('Carlos'))  # Carlos <class 'str'>

# Idade: Inteiro(Int)
print(33, type(33))  # 33 <class 'int'>

# Altura: float(decimal)
print(1.80, type(1.80))  # 1.8 <class 'float'>

# Maio de Idade? : Booleano (bool)
print(33 > 18, type(33 > 18))  # True <class 'bool'>
